class Informer(nn.Module):
    def __init__(self, enc_in, d_model=512, n_heads=8, e_layers=5, k_size=3, d_ff=512, dropout=0.1, factor=5, # e_layers=6, 3 7
              attn='prob', activation='gelu', output_attention = False, distil=True, mix=True, cnn_attn='cbam', window_size=250):
        
        super(Informer, self).__init__()
        #self.pred_len = out_len
        
        self.attn = attn
        self.output_attention = output_attention

        # Encoding
        self.enc_embedding = DataEmbedding(enc_in, d_model, dropout)

        # Attention
        Attn = ProbAttention if attn=='prob' else FullAttention
        # Encoder
        
        self.encoder = Encoder(
            [
                EncoderLayer(
                    AttentionLayer(Attn(False, factor, attention_dropout=dropout, output_attention=output_attention), 
                                d_model, n_heads, mix=False),
                    d_model,
                    d_ff,
                    dropout=dropout,
                    activation=activation
                ) for l in range(e_layers)
            ],
            [
                ConvLayer(
                    d_model, k_size=k_size #8-e_layers
                ) for l in range(e_layers)
            ] if distil else None,
            norm_layer=torch.nn.LayerNorm(d_model)
        )
        
        
        self.cnn_attn = cnn_attn
        
        if self.cnn_attn == 'cbam':
            #self.cbam0 = cbam(256, ratio=16)
            self.cbam = cbam(d_model, ratio=16)
            
        serial_length = window_size*2
        
        l_nums = serial_length // (k_size**e_layers)

        self.dense1 = nn.Linear(l_nums*d_model, 1024) # 1024

        self.dense2 = nn.Linear(1024, 256)
        
    def forward(self, x):
        
        enc_out = self.enc_embedding(x) # 32 18 256
        enc_out, attns = self.encoder(enc_out, attn_mask=None)
        
        
        if self.cnn_attn == 'cbam':
            enc_out = enc_out.transpose(1, 2)
            enc_out = self.cbam(enc_out)
            
        enc_out = enc_out.contiguous().view(enc_out.shape[0], -1)
        
        enc_out = self.dense1(enc_out)
            
        enc_out = self.dense2(enc_out)
        
        return enc_out

